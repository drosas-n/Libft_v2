#include "libft.h"
#include <stdio.h>

int	ft_atoi(const char *s)
{
	int	i;
	int	nb;
	int	neg;

	i = 0;
	nb = 0;
	neg = 1;
	if (s[0] == '-' || s[0] == '+')
	{
		if (s[0] == '-')
			neg = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = (nb * 10) + (s[i] - 48);
		i++;
	}
	return (nb * neg);
}
